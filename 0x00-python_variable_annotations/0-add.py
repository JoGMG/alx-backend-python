#!/usr/bin/env python3
"""
A type-annotated function `add` that takes a float `a` and
a float `b` as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float type numbers and
    returns their float type sum.

    Argument:
        - `a`: float type number
        - `b`: float type number
    """
    return a + b
